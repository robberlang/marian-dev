static constexpr int lenListHTMLEntities = sizeof(listHTMLEntities) / sizeof(HTMLEntity);

EntityRep getCharRepOfEntity(const char* entityName) {
  int lowOfs = -1;
  int hiOfs = lenListHTMLEntities;
  while(lowOfs + 1 < hiOfs) {
    // Calc the mid point of the low and high offset.
    const int midOfs = (hiOfs + lowOfs) >> 1;

    //
    //  If our test char is greater than the mid point char, then
    //  we move up to the upper half. Else we move to the lower
    //  half. If its equal, then its our guy.
    //
    int comp = std::strcmp(entityName, listHTMLEntities[midOfs].entityName);
    if(comp > 0) {
      lowOfs = midOfs;
    } else if(comp < 0) {
      hiOfs = midOfs;
    } else {
      return listHTMLEntities[midOfs].entityRep;
    }
  }

  return EntityRep(-1);
}

